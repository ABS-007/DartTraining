void main() {
  var gfg = ['Geeks'];

  // Printing all the values in List
  print(gfg);

  // Adding new value in List at
  // specific index and printing it

  // list_name.insertAll(index, list_of_values);
  gfg.insertAll(1, ['For', 'Geeks']);
  print(gfg);

  // Element at index 1 in list
  print(gfg[1]);
}
